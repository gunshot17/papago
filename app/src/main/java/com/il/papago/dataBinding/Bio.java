package com.il.papago.dataBinding;

public class Bio {
    private String name;
    private String email;

    public Bio(){

    }

    public Bio(String name, String email) {
        this.name = name;
        this.email = email;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
